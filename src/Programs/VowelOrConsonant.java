package Programs;

public class VowelOrConsonant {

	public static void main(String[] args) {
		
		//a e i o u vowel
		
		char ch = 'z';
		
		if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') {
			System.out.println(ch + " is Vowel");
		}
		
		else {
			System.out.println(ch + " is Consonant");
		}
	} 

}
